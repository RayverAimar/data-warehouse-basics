-- Crear una función para generar un rango de fechas
CREATE OR REPLACE FUNCTION generate_dates(ini DATE, fin DATE)
RETURNS TABLE(fecha DATE) AS $$
BEGIN
    RETURN QUERY SELECT generate_series(ini, fin, interval '1 day')::date;
END;
$$ LANGUAGE plpgsql;

-- Insertar datos en la la tabla Fecha con todas las fechas de 2023
INSERT INTO Fecha (Dia, Mes, Anio, Dia_Semana)
SELECT EXTRACT(DAY FROM fecha) AS Dia,
       EXTRACT(MONTH FROM fecha) AS Mes,
       EXTRACT(YEAR FROM fecha) AS Anio,
       TO_CHAR(fecha, 'Day') AS Dia_Semana
FROM generate_dates('2023-01-01'::DATE, '2023-12-31'::DATE);

